function dateToTarot(dateStr) {
    let num = dateStr.replace(/-/g, '').split('').reduce((a, b) => a + parseInt(b), 0);
    while (num > 21) num = num.toString().split('').reduce((a, b) => a + parseInt(b), 0);
    return num;
}

